#include <stdio.h>

int main() {
    int numbers[5];
    int evenCount = 0, oddCount = 0;
    

    printf("Nhap vao 5 so nguyen: ");
    for (int i = 0; i < 5; i++) {
        numbers[i];
    }

    
    for (int i = 0; i < 5; i++) {
        if (numbers[i] % 2 == 0) {
            evenCount++;
        } else {
            oddCount++;
        }
    }

    
    printf("So luong so chan: ");
    printf("So luong so le: ");

    return 0;
}




